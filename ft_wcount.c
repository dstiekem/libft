#include "libft.h"

unsigned int	wcount(char const *s, char c)
{
	unsigned int	i;

	i = 0;
	while (*s)
	{
		if (*s != c && (*(s + 1) == c || *(s + 1) == '\0'))
			i++;
		s++;
	}
	return (i);
}
